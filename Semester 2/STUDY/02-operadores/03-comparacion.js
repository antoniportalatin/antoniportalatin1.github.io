// Operadores Relacionales
let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// Operadores de igualdad
console.log(a == 10); // == igual a (comparaciÃ³n).
console.log(a != 10);// != no es igual a.
// el == solo verifica si el valor es igual, no los tipos.
console.log(a == '10'); // por ende, esto es true ya que no compara los tipos.
// Para verificar valor y tipo debemos usar ===.
console.log(a === '10'); // da false
console.log(a !== '10');// da true, ya que esta comprobando que son de diferentes tipos o diferente valor.