// Funciones: se crean usando la palabra reservada function seguido por un nombre + () + {}.
function saludar(){
    console.log('Hola Mundo');
}

// Para usar la funcion previamente creada, debo usar el nombre de la función + () +;
saludar();

// Para finalizar una función y devolver el valor de esta misma, usamos return.
function suma(){
    return 2+2;
}
let resultado = suma();
console.log(resultado);